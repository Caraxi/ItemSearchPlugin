        public static string DescriptionAttr<T>(this T source) {
            FieldInfo fi = source.GetType().GetField(source.ToString());

            DescriptionAttribute[] attributes = (DescriptionAttribute[])fi.GetCustomAttributes(
                typeof(DescriptionAttribute), false);

            if (attributes != null && attributes.Length > 0) return attributes[0].Description;
            else return source.ToString();
        }

        public static string? GetExpac(string patch)
        {
            if (float.TryParse(patch, out float number))
            {
                switch (number)
                {
                    case >= 1 and <2:
                        return "1.0";
                        break;
                    case >= 2 and < 3:
                        return "ARR";
                        break;
                    case >= 3 and < 4:
                        return "HW";
                        break;
                    case >= 4 and < 5:
                        return "SB";
                        break;
                    case >= 5 and < 6:
                        return "ShB";
                        break;
                    case >= 6 and < 7:
                        return "EW";
                        break;
                    case >=7 and <8:
                        return "DT";
                        break;
                }
            }
            return patch;
        }

        // Absolutely shamelessly stolen from https://raw.githubusercontent.com/Kouzukii/ffxiv-whichpatchwasthat/refs/heads/main/WhichPatchWasThat/ItemPatchMapper.cs
        public static string? GetPatch(ulong id) {
            if (id > uint.MaxValue) return null;

            var itemId = (uint)id;
            var min = 0;
            var max = PatchRanges.Length - 1;

            while (min <= max) {
                var index = min + ((max - min) / 2);
                var range = PatchRanges[index];

                if (itemId < range.Start) {
                    max = index - 1;
                } else if (itemId > range.End) {
                    min = index + 1;
                } else {
                    return range.Patch;
                }
            }

            return null;
        }

